import Foundation
import UIKit

///SUM OF TWO NUMBERS
public func SumOfTwoNumbers(num1: Int, num2: Int)-> Int{
   return num1 + num2
}

///SUBSTRACTION OF TWO NUMBERS
public func SubtractionOfTwoNumbers(num1: Int, num2: Int)-> Int{
   return num1 - num2
}

///MULTIPLY OF TWO NUMBERS
public func MultiplyOfTwoNumbers(num1: Int, num2: Int)-> Int{
   return num1 * num2
}

///SIMPLE INTEREST
public func SimpleInterest(num1: Int, num2: Int, num3: Int)-> Int{
   return (num1 * num2 * num3)/100
}




